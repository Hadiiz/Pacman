export function generateGraph(arr, rows, cols) {
  for (let i = 0; i < rows; i++) {
    arr[i] = [];
    for (let j = 0; j < cols; j++) {
      arr[i][j] = new Cell(j, i);
      if (data[i][j] == 1) {
        arr[i][j].wall = true;
      }
    }
  }
}

